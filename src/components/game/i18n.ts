const DICTS: Record<Language, PartialDict> = {
  en, es, pt, fr, de, it, pl, ru, tr, ja, ko, ar,
};

export function getT(lang: Language): (key: UIKey) => string {
  const d = DICTS[lang] ?? en;
  return (key) => d[key] ?? en[key] ?? key;
}